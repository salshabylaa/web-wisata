                                <td><h3>Hak Asasi Manusia di Era Digital</h3></td>
                            </tr>
                            <tr>
                                <td>Apa kaitan antara hak asasi manusia dan era digital? Benarkah era ini mengubah karakteristik konvensional 
                                    konsep hak-hak asasi manusia?</td>
                            </tr>
                            <tr>
                                <td>Hak asasi manusia adalah hak yang melekat secara kodrati, yang eksis bukan karena diberikan oleh hukum positif. 
                                    Jadi, hak asasi manusia adalah hak yang hadir dan berlaku secara pra-positif. Dengan demikian, era digital 
                                    sesungguhnya tidak menggeser pemaknaan konseptual ini kendati mungkin secara denotatif, akan ditemukan 
                                    format-format baru dari hak asasi manusia ini.</h4></td>
                            </tr>
                            <tr>
                                <td>Secara  historis, sebagaimana kita pelajari di bangku-bangku kuliah, konsep hak asasi manusia memang lahir di 
                                    Barat, yakni diawali dari Magna Charta Libertatum tahun 1251 di Inggris, dilanjutkan dengan deklarasi Habeas 
                                    Corpus tahun 1679. Inti dari konsep hak asasi manusia yang paling awal ini adalah hak-hak terkait dengan 
                                    pemidanaan, seperti larangan untuk melakukan penahanan dan penyitaan secara sewenang-wenang.</td>
                            </tr>
                            <tr>
                                <td>ada kurun waktu itu pula, filsuf Inggris dan “Bapak Liberalisme” bernama John Locke (1632-1704) mengemukakan 
                                    teorinya tentang kontrak sosial. Pemikirannya sangat mempengaruhi konsep hak asasi manusia karena di dalam 
                                    teori ini dikemukakan konsep persamaan kedudukan manusia di hadapan hukum. Semasa John Locke hidup, terjadi 
                                    perubahan sistem ketatanegaraan yang penting di Inggris, yaitu disahkannya Bill of Rights (1689) yang 
                                    memberikan hak-hak khusus kepada parlemen di negara itu dan hak-hak setiap warga negara, seperti hak untuk 
                                    hidup, hak milik, hak memperoleh kebahagiaan, dan lain-lain. Perkembangan ini menandai masuknya konsep hak 
                                    asasi manusia ke dalam konstitusi, sehingga akhirnya hak asasi manusia menjadi hak konstitusional 
                                    (constitutional rights). Apa yang terjadi di Inggris ini mempengaruhi gerakan konstitusionalisme di 
                                    berbagai negara lain, sehingga pada akhirnya masuk juga ke dalam peraturan perundang-undangan di Indonesia.</td>
                            </tr>
                            <tr>
                                <td>Bahkan di dalam Kitab Undang-Undang Hukum Perdata, yang berlaku jauh sebelum Indonesia merdeka (tentu berarti 
                                    juga saat itu belum ada konstitusi negara), konsep hak asasi manusia sudah diakomodasi, antara lain dalam 
                                    Pasal 3. Di situ dikatakan: “Geenerlei straf heeft den burgerlijken dood of het verlies van alle burgerlijke 
                                    regten ten gevolge.” (Tiada suatu hukuman pun mengakibatkan kematian perdata atau kehilangan segala hak-hak 
                                    perdata). Jadi, tidak boleh ada penghukuman (penjatuhan sanksi) yang membuat seseorang sampai kehilangan 
                                    hak-hak keperdataannya. Apabila ranah hukum privat pun sudah mensyaratkan ini, bahkan sejak dua abad yang 
                                    lalu, maka tidak ada alasan bahwa ranah hukum publik untuk tidak mengakomodasinya.</td>
                            </tr>
                            <tr>
                                <td>Dari rumusan kata-kata “Tiada suatu hukuman pun…” sebenarnya dapat diduga bahwa rumusan Pasal 3 KUH Perdata itu 
                                    mendapat pengaruh kuat dari aliran Liberalisme. Ciri dari formulasi hak asasi manusia ala liberalisme memang 
                                    banyak menggunakan kata-kata negatif, dalam arti memuat larangan secara selektif (sebenarnya juga minimalis) 
                                    bagi negara untuk berbuat sesuatu yang dapat mencederai hak-hak asasi warganya. Di sini posisi warga dalam 
                                    keadaan pasif, yakni tinggal menerima (pasrah) dan yakin pada komitmen negara atas pengakuan hak-hak asasi 
                                    manusia yang dijanjikannya.</td>
                            </tr>
                            <tr>
                                <td>Dalam perkembangannya, tentu saja rumusan negatif seperti di atas sudah tidak lagi dianggap cukup. Hak asasi 
                                    manusia harus diperjuangkan melalui sistem ketatanegaraan yang demokratis. Hak-hak asasi manusia pun bertumbuh, 
                                    tidak lagi semata menunggu sampai negara memasukkannya ke dalam konstitusi. Oleh sebab itu, tidaklah 
                                    mengherankan apabila corak hak asasi manusia tidak lagi selalu harus universal. Ada sejumlah hak yang berlaku 
                                    partikular, dalam arti hak-hak yang unik diterima dan diakui ada pada satu kelompok tertentu karena faktor 
                                    budaya, agama, dan sebagainya.</td>
                            </tr>
                            <tr>
                                <td>Pemahaman tentang hak asasi manusia seperti di atas, sangat penting disampaikan sejak awal kepada para penstudi 
                                    hukum karena fondasi berpikir inilah yang akan mereka gunakan untuk mencermati perkembangan konsep-konsep hak 
                                    asasi manusia. Tidak terkecuali, terhadap hak-hak yang diklaim sebagai hak asasi manusia di era digital.</td>
                            </tr>
                            <tr>
                                <td>Era digital bersentuhan dengan satu era tersendiri di dalam wacana filsafat, yaitu era dekonstruksi kebenaran. 
                                    Orang ramai-ramai menyebutnya sebagai era post-truth. Era ini disebut sebagai zaman yang meletakkan 
                                    “seni kebohongan” (the art of the lie) sebagai instrumen dalam penyampaian gagasan. Era tatkala hoaks (hoax) 
                                    difabrikasi secara masif dan sistematis untuk mendukung kekuasaan petahana atau syahwat meraih kekuasaan calon 
                                    penguasa. Pengertian kekuasaan ini harap dibaca secara luas, karena dapat terkait dengan politik, ekonomi, 
                                    psikologis, kultural, dan lain-lain.</td>
                            </tr>
                            <tr>
                                <td>Salah satu hak asasi manusia yang muncul belakangan, khususnya terkait dengan hak warga sebagai konsumen 
                                    vis-a-vis hak para pelaku usaha, adalah hak atas informasi yang benar (the right to be informed). Berbohong 
                                    pada awalnya dianggap wajar dalam dunia bisnis, sebagaimana kebohongan yang lazim kita saksikan pada iklan-iklan 
                                    di media massa. Masyarakat sepertinya maklum karena dalam bisnis “tidak ada kecap yang tidak menyebut dirinya 
                                    nomor satu”.  Namun, ada kalanya pelaku usaha tidak lagi bisa ditoleransi karena kebohongan itu sudah mengarah 
                                    pada penipuan. Jadi ada pergeseran dari “fib to fraud”.</td>
                            </tr>
                            <tr>
                                <td>Digitalisasi informasi sungguh-sungguh berperan mempercepat pergerakan “fib to fraud” ini. Dan, sekali lagi hal 
                                    ini berdampak sangat dahsyat pada konstruksi kepercayaan (trust) masyarakat terhadap struktur sosial yang ada, 
                                    termasuk lembaga-lembaga yang selama ini dianggap mapan, seperti lembaga negara, agama, pendidikan, dan 
                                    sebagainya. Percepatan itu terjadi karena repetisi yang sangat luar biasa terhadap kebohongan atau potensi 
                                    kebohongan. Percepatan yang mungkin di luar dugaan ahli dan praktisi hoaks sekaliber Joseph Goebbels 
                                    (1897-1945), Menteri Propaganda Pemerintahan Nazi Jerman), yang terkenal dengan pernyataannya: “A lie told once 
